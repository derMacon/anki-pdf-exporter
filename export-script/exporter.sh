#!/bin/bash

func_usage() {
(echo "Usage:
exporter.sh -h | exporter.sh --help
  prints this help and exits
exporter.sh INPUT 
  INPUT is a valid deck file (.txt generated by anki)")
}


# Checks if there is only one arg and if the help is desired or not
if [ "$#" -ne 1 ] || [ "X$1" = "X-h" ] || [ "X$1" = "X--help" ]
then
		func_usage
else
		java -jar .txt-to-tex-exporter.jar ./test.txt

		cd output/
		file_name=$(basename "$1" .txt)
		./../.tex-to-pdf-exporter.sh $file_name.tex
fi

